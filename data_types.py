# integer -> whole numbers -> int
a=3
print(a)

# floating number -> Numbers with decimal point -> float
a=4.3
b=3.5
c=100.0
print(a)
print(b)
print(c)

# strings -> Ordered sequence of characters > str
a="hello"
print(a)

# lists -> ordered sequence of objects -> list
a = [10, "hello", 10.0]
print(a)

# dictionaries -> unorder key:value pairs -> dict
a = {"myKey":"value", "name":"franklin"}
print(a)

# Tuples -> ordered immutable sequence of objects -> tup
a = (10,"hello",20.3)
print(a)

# sets -> unordered collection of unique objects -> set
a={"a","b"}
print(a)

# booleans -> logical value indicatin True or False ->  bool
a=True
print(a)