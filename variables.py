# Rules for variables
# -> Cannot start with number
# -> There can be no spaces , use _ instead
# -> Can't use of any special characters
# -> Use lowercase
# -> Avoid using special meaning in python like "str" and "list"
# -> Python uses Dynamic Typing
# -> This means you can reassign variables to different data types
# -> Use type() to get type

my_var = 2
print(my_var)

my_var = ["hello",2]
print(my_var)

print(type(my_var))
